    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Statement st; 
        ResultSet rs;
        DefaultListModel list = new DefaultListModel();
        Conexao conex = new Conexao();
        String sql = "select * from tb_produtos";
        conex.conectar();
        try{
            st = Conexao.con.createStatement();
            rs = st.executeQuery(sql);
            if(!rs.next()){
                JOptionPane.showMessageDialog(null, "NÃ£o existe produto cadastrado! =(");
                return;
            }
            while(rs.next()){
                 list.addElement(rs.getInt("id_produto") + "-" +rs.getString("nome_produto")+ "-" +rs.getString("valor_produto")+ "-" +rs.getString("dt_validade"));
            }
            lsProdutos.setModel(list);
        }catch(SQLException ex){
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

