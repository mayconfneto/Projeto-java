    private void btCadClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadClienteActionPerformed
        Statement meustate;
        Conexao conex = new Conexao();  
         
        String sql = "insert into tb_cliente (nome,telefone,telefone2,email,rg,cpf,endereco) "
        + "values('"+ txtNome.getText()+"','"+ txtTelefone.getText() +"','"+ txtTelefone2.getText()+"','"+ txtEmail.getText()+"','"+ txtRG.getText()+"','"+ txtCPF.getText()+"','"+ txtEndereco.getText()+"')";
         
        conex.conectar();
         
        try{
             meustate = Conexao.con.createStatement();
             meustate.executeUpdate(sql);
             txtNome.setText("");
             txtTelefone.setText("");
             txtTelefone2.setText("");
             txtEmail.setText("");
             txtRG.setText("");
             txtCPF.setText("");
             txtEndereco.setText("");
             JOptionPane.showMessageDialog(null,"Cliente cadastrado com sucesso");
         }
         catch(SQLException erro){
             //erro
         }
        conex.desconectar();
    }//GEN-LAST:event_btCadClienteActionPerformed

