    private void btPesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisaActionPerformed
        // TODO add your handling code here:
        String produto = txtProduto.getText();
        Statement meustate;
        ResultSet rs;
        DefaultListModel list = new DefaultListModel();
        Conexao conex = new Conexao();  
         
        String sql = "select * from tb_produtos where alias = '"+produto+"'";
         
        conex.conectar();
        try{
        meustate = Conexao.con.createStatement();
        rs =  meustate.executeQuery(sql);
        if(!rs.next()){
            JOptionPane.showMessageDialog(null, "Produto não encontrado!");
            return;
        }else{
            list.addElement(rs.getInt("id_produto") + "-" +rs.getString("nome_produto")+ "-" +rs.getString("valor_produto")+ "-" +rs.getString("dt_validade"));
        }
        txtListProd.setModel(list);
        }catch(SQLException ex){
            System.out.print(ex.getMessage());
        }
    }//GEN-LAST:event_btPesquisaActionPerformed

